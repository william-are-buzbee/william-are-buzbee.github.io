// Sprite bake cache
export const spriteCache = {};
function buildSprite(rows){
  const c = document.createElement('canvas');
  c.width = SPR*PIX; c.height = SPR*PIX;
  const g = c.getContext('2d');
  g.imageSmoothingEnabled = false;
  for (let y=0;y<SPR;y++){
    const row = rows[y]||'';
    for (let x=0;x<SPR;x++){
      const ch = row[x]||'.';
      if (ch === '#') g.fillStyle = COL_FG;
      else if (ch === '-') g.fillStyle = COL_MID;
      else continue;
      g.fillRect(x*PIX, y*PIX, PIX, PIX);
    }
  }
  return c;
}
