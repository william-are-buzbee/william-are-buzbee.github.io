// Terrain tint cache
const tintedCache = {};
function buildTintedTerrain(spriteName, palette){
  const rows = getActiveSprite(spriteName);
  const c = document.createElement('canvas');
  c.width = SPR*PIX; c.height = SPR*PIX;
  const g = c.getContext('2d');
  g.imageSmoothingEnabled = false;
  g.fillStyle = palette.bg;
  g.fillRect(0, 0, c.width, c.height);
  for (let y=0;y<SPR;y++){
    const row = rows[y]||'';
    for (let x=0;x<SPR;x++){
      const ch = row[x]||'.';
      if (ch === '#') g.fillStyle = palette.fg;
      else if (ch === '-') g.fillStyle = palette.mid;
      else continue;
      g.fillRect(x*PIX, y*PIX, PIX, PIX);
    }
  }
  return c;
}

// Cave wall renders as dark warm stone — visible to native eyes, not pure black
const CAVE_WALL_DARK = { bg: '#1a1614', fg: '#342e28', mid: '#262220' };

export function tintedSprite(spriteName, paletteName){
  const key = spriteName + '|' + paletteName;
  if (tintedCache[key]) return tintedCache[key];
  // Force dark warm rendering for cave walls — visible, not pitch-black
  if (paletteName === 'cave_wall') {
    tintedCache[key] = buildTintedTerrain(spriteName, CAVE_WALL_DARK);
    return tintedCache[key];
  }
  const pal = BIOME[paletteName];
  if (!pal){
    // Fallback: if palette not found, use plains
    console.warn('Missing palette:', paletteName, 'for sprite:', spriteName);
    tintedCache[key] = buildTintedTerrain(spriteName, BIOME.plains);
    return tintedCache[key];
  }
  tintedCache[key] = buildTintedTerrain(spriteName, pal);
  return tintedCache[key];
}

// Monster tinting
const tintedMonCache = {};
export function tintedMonsterSprite(spriteName, tintColor){
  if (!tintColor) return spriteCache[spriteName];
  const key = spriteName + '|' + tintColor;
  if (tintedMonCache[key]) return tintedMonCache[key];
  const rows = S[spriteName];
  const c = document.createElement('canvas');
  c.width = SPR*PIX; c.height = SPR*PIX;
  const g = c.getContext('2d');
  g.imageSmoothingEnabled = false;
  const darker = mixColors(tintColor, '#000000', 0.3);
  for (let y=0;y<SPR;y++){
    const row = rows[y]||'';
    for (let x=0;x<SPR;x++){
      const ch = row[x]||'.';
      if (ch === '#') g.fillStyle = tintColor;
      else if (ch === '-') g.fillStyle = darker;
      else continue;
      g.fillRect(x*PIX, y*PIX, PIX, PIX);
    }
  }
  tintedMonCache[key] = c;
  return c;
}

function mixColors(a, b, t){
  const pa = parseInt(a.slice(1),16);
  const pb = parseInt(b.slice(1),16);
  const ar=(pa>>16)&255, ag=(pa>>8)&255, ab=pa&255;
  const br=(pb>>16)&255, bg=(pb>>8)&255, bb=pb&255;
  const r=Math.round(ar*(1-t)+br*t), gg=Math.round(ag*(1-t)+bg*t), bl=Math.round(ab*(1-t)+bb*t);
  return '#' + ((r<<16)|(gg<<8)|bl).toString(16).padStart(6,'0');
}

