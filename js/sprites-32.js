//   '-...............................' = mid-tone color  (COL_MID or tint shadow)
//   '................................' = transparent
//
// Canvas: SPR=32, PIX=1 → 32×32 canvases (same physical size as 16px pack's
// SPR=16, PIX=2). Both packs produce identically-sized canvases; this one
// simply has 4× the logical pixel count for finer detail.

